        <hr>
        <h5><u>Chemical safety</u></h5>
        <ul>
          <li>Consider all chemicals potentially hazardous. Strictly avoid the contact of the chemicals with mouth, skin or eyes.</li>
          <li>A fume cupboard must always be used for work involving toxic, fuming, or unpleasant smelling chemicals.</li>
          <li>Immediate report of any spillage to your supervisor.</li>
          <li>Don’t neutralise acid with alkali or vice versa. Contaminated clothing must be removed immediately and the skin flushed with water for fifteen minutes. Decontaminate the area using a mild detergent and water.</li>
          <li>In case of volatile, flammable material, ventilate the area, don’t operate electric switches.</li>
          <li>For skin contact, immediately wash with soap or mild detergent. Remove contaminated clothing and shoes immediately if necessary.</li>
          <li>For eyes, immediately flush with water for at least 15 minutes (lifting upper and lower eyelids occasionally) and obtain medical attention. If inhaled, move to fresh air immediately and seek medical attention.</li>
          <li>In case of ingestion, seek medical attention immediately.</li>
        </ul>
        <hr>
        <h5><u>Liquid nitrogen</u></h5>
        <ul>
          <li>Take proper precautions while moving containers of liquid nitrogen.</li>
          <li>Always handle liquid nitrogen in well ventilated area to avoid displacement of oxygen from air.</li>
          <li>Always protect your face with a face shield. Always wear protective cryo gloves. Filling a container, or inserting objects into liquid nitrogen, should always be done slowly, in order to minimise boiling and splashing. Always use plastic vials in preference to glass.</li>
          <li>Never touch liquid nitrogen or materials frozen in it with bare hands. Use forceps to withdraw objects immersed in the liquid.</li>
          <li>Vials withdrawn from the liquid phase may shatter explosively and should be placed in a secondary container to prevent dissemination of materials.</li>
        </ul>
        <hr>
        <h5><u>Radiation safety</u></h5>
        <ul>
          <li>Experiments involving radioactivity should be handled only in the room specified for the purpose.</li>
          <li>NIT Durgapur facility is a type II category. Only authorized personnel are allowed to enter the facility.</li>
          <li>Every person who wishes to use radioactive substances must register with the Radiation Safety Officer.</li>
          <li>Purchase of all radioactive biomolecules is done through the RSO. Once the material is received, it is stored under lock and key in a deep-freezer.The issuance of radioactive molecules to the user should be recorded.</li>
        </ul>
        <hr>
        <h5><u>Ultraviolet radiation:</u></h5>
        <ul>
          <li>Ultraviolet radiation has relatively low penetrating power and hence affects only the eyes and skin.</li>
          <li>Gross overexposure to UV can cause severe ‘sunburn’ and may even cause skin cancer.</li>
          <li>Early effects on the eye include inflammation of the surface conjunctiva and cornea. In some cases of the damage to the cornea becomes irreversible.</li>
          <li>While using trans-illuminators and UV torches, use vinyl gloves, UV-opaque goggles and screens to prevent exposure. Extreme care must be taken to avoid accidental exposure of other persons.</li>
        </ul>
        <hr>
        <h5><u>Fire:</u></h5>
        <ul>
          <li>Always be careful while using instruments generating high temperatures.</li>
          <li>Bunsen burner in the biosafety hood, hot plate, incubator, hot air oven, microwave oven is all potential sources of fire in the lab. Switch them off if you want to attend to something else.</li>
          <li>Learn how to use the fire extinguisher that is placed outside the lab.</li>
        </ul>
        <hr>
        <h5><u>Waste disposal:</u></h5>
        <ul>
          <li>Use colored bins and plastic bags (labelled to indicate biohazard) to collect wastes resulting from experiments.</li>
          <li>The wastes generated in the lab (except radioactive waste which is treated differently) should be kept in a separate specific colored bin. It does not leak, spill or blow away and is protected against scavenging by people or animals. It needs decontamination before disposal.</li>
          <li>Hypodermic needles should be burned with a syringe destroyer.</li>
        </ul>
        <hr>
        <h5><u>Remember...</u></h5>
        <ul>
          <li>No one is really safe in a work area that contains one careless worker.</li>
          <li>If you think that your colleagues are careless – explain the dangers to
          them.</li>
          <li>If this does not help, do not hesitate to bring the matter to the attention of your supervisor. Discuss safety openly with your colleagues.A safe laboratory is a nice place to be in!</li>
        </ul>
