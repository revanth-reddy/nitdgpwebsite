      <div class="mtitle">General Safety</div>
      <div class="mbody">Safety in work place and safety of the society is our major concern. Creating safe environment for carrying out research activities and maintaining safe environment around us is our responsibility. It requires proper safety measures and appropriate precautions in laboratories.<br>
        <h5><u>Laboratory habits:</u></h5>
        <ul>
          <li>Clean working habit.</li>
          <li>Immediate action for spilling reagents.</li>
          <li>Hot materials such as autoclaved media and glassware, corrosive acids, organic solvents etc should be transported using a tray or trolley depending on the amount and weight.</li>
          <li>Never store heavy items or hazardous substances on high shelves.</li>
          <li>Use proper stand for Gas cylinder or liquid nitrogen tank. Always use a cylinder trolley to transport cylinders.</li>
        </ul>
        <hr>
        <h5><u>Personal safety:</u></h5>
        <ul>
          <li>Eating, chewing and drinking, and application of cosmetics in laboratory working areas are strictly forbidden. Food and drinks should not be stored in refrigerators.</li>
          <li>Avoid touching the mouth or eyes while working.</li>
          <li>Do not lick labels.</li>
          <li>Never pipette by mouth.</li>
          <li>Don’t ignore to minor injury like small cuts and burns. Viruses, bacteria, fungi and harmful chemicals can enter your body through unprotected wounds.</li>
          <li>Do not attempt to handle heavy or awkward loads on your own without the use of appropriate mechanical aids.</li>

          <li>Pregnant women should not be exposed to hazardous substances,
          radiations or undertake hazardous activities.</li>
          <li>Talk to the supervisor if you are uncertain of the safety of a material or protocol.</li>
          <li>Never work alone in the lab, especially in the night and on holidays.</li>
        </ul>
        <hr>
        <h5><u>Personal Protective Equipment (PPE)</u></h5>
        <ul>
          <li>Wear lab coat, goggles and use gloves while handling hazardous materials.</li>
          <li>An appropriate mask/respiratory protection gear must be worn when handling chemicals such as SDS or when working with infectious agents that are transmitted through air.</li>
          <li>Suitable safety spectacles or a face shield must be worn when carrying out procedures which involve a risk of splash or impact by harmful materials or risk of exposure to UV radiation.</li>
          <li>Don’t use mobile phone while working in the lab, especially while you’re working in the hood, handling human/animal tissues or doing experiments with hazardous, toxic chemicals and radioactivity. Do not touch door handles, elevator buttons or phones with gloved hands.</li>
        </ul>
        <hr>
        <h5><u>Equipment and apparatus</u></h5>
        <ul>
          <li>Do read the operation manual before using equipment. Do not operate machines that you are not familiar with.</li>
          <li>Never leave instruments running unattended.
          <li>Never try to repair any equipment. Immediately report to the concerned people.</li>
          <li>Ensure that all safety equipment such as radiation monitors, fire extinguishers, microbiological safety cabinets, fume cupboards are in working condition.</li>
        </ul>
        <hr>
        <h5><u>Biological safety</u></h5>
        <ul>
          <li>You need to take appropriate precautions before embarking on a specific work and these depend on the materials you handle. Talk to your supervisor about the risks involved and the ways to minimize them. Use appropriate containment facility and follow the safety regulations.</li>
        </ul>
        <hr>
        <h5><u>Human tissues and body fluids</u></h5>
        <ul>
          <li>Take proper precaution while handling clinical specimens as they may contain pathogenic viruses, bacteria or fungi.</li>
          <li>Never let them come into direct contact with skin. Cuts or even invisible abrasions are an easy way into the blood stream. Take care not to handle them without wearing gloves of appropriate grade.</li>
          <li>Working with human blood is required to be vaccinated against Hepatitis B.</li>
        </ul>
        <hr>
        <h5><u>Genetic modification</u></h5>
        <p>All work involving genetic manipulations of microorganisms, plants and animals should be carried out as per the regulations of Department of Biotechnology (DBT), Government of India and should be approved by the Institutional Biosafety Committee (IBSC).</p>
        <h5><u>Microorganisms</u></h5>
        <ul>
          <li>In case of microorganisms, the pathogenicity of the organism is extremely important for the risk assessment and subsequent
          categorization.</li>
          <li>The host must be evaluated to determine that it is not pathogenic.</li>
          <li>The microorganisms have been categorized based on infectivity towards humans into four groups out of which the first group is that of non- pathogens (Table 2).</li>
          <li>This categorization is generally applicable only for the assessment of containment. Specific measures is required for working with the specific group of containment to ensure that the organism do not infect the laboratory peoples and the society around.</li>
        </ul>
        <hr>
        <p>Table: Categorization of microorganisms based on pathogenicity</p>
